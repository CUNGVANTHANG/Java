package chapter_2;

public class Bai_tap_vong_lap_for {

    public static void main(String[] args) {
        // run a for loop from i = 5 to 1
        // print the value of i
        // Hint: Use -- operator to decrease the value of i
        for (int i = 5; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
