public class Array {
    public static void main (String[] args) {
        int[] age = {24, 23, 18, 19, 20};
        for (int i = 0; i < age.length; i++) {
            System.out.println(age[i]);
        }
        // Trong đó: 
        // age - tên của mảng
        // [] - biểu thị đây là một mảng
        // {24, 23, 18, 19, 20} - các phần tử mảng
        // int - cho biết mảng chí có thể chứa các phần tử số nguyên
    }
}
