public class method_random {
    int randomNumber = (int) (Math.random() * 10 + 1);
}

// Math.random() - tạo số ngẫu nhiên từ 0,0 đến nhỏ hơn 1,0
// Math.random() * 10 - tạo số ngẫu nhiên từ 0,0 đến nhỏ hơn 10,0
// Math.random() * 10 + 1 - tạo số ngẫu nhiên từ 1,0 đến 10,0
// (int) - chuyển đổi số ngẫu nhiên thành số nguyên (từ 1 đến 10)